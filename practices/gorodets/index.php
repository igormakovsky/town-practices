    <section class="content text">

        <h2>Место действия</h2>
        <p>Уникальное культурное наследие в сфере народных художественных промыслов — один из ключевых ресурсов развития туризма в Нижегородской области. Городец, в свою очередь, яркий пример того, как в самом древнем городе региона можно создать туристский центр, обладающий статусом «места бытования народных промыслов».</p>
        <p>Городец — небольшой провинциальный город на севере области, некогда бывший столицей самостоятельного княжества. Один из древнейших русских городов на всей Средней Волге, упоминается с 1152 г. Городом в свое время правили сыновья и брат Александра Невского князь Андрей, а сам великий князь-воин и князь-дипломат окончил здесь в 1263 году свой жизненный путь. С конца XVIII века город известен как центр деревянного судостроения, хлебной торговли, выпечки печатных пряников, и как пункт сбыта кустарных изделий из дерева — так называемого «щепного товара» (деревянной посуды, прялок и пр.).</p>

