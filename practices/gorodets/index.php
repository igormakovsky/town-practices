        <h2>От идеи к ее вплощению</h2>
        <p>В целях сохранения уникальной глухой резьбы по дереву и демонстрации традиций деревянного зодчества Нижегородского края в 2002 году региональная туристская администрация запланировала проект по созданию центра деревянного зодчества. Чтобы народные промыслы Городецкого района были представлены там в «живом» формате.</p>
        <p>Замысел заключалась в том, чтобы создать площадку для эффективной интеграции места бытования народных художественных промыслов и разнообразных туристских форматов, на которой можно было бы разработатьширокую линейку оригинальных туристских продуктов в новом, максимально ярком и качественном виде.</p>
        <p>МТК «Город мастеров» был построен в 2009 году на средства Правительства Нижегородской области и администрации Городецкого района. Общий объем бюджетных средств составил 92 млн.руб.</p>
        <p>Один из авторов и главный вдохновитель проекта создания МТК «Город мастеров» в Городце — Алла Александровна Морозова. Историк, экономист и социолог, она вот уже более 20 лет успешно работает в сфере туризма, преимущественно на госслужбе. В настоящее время А.А. Морозова возглавляет управление по развитию туристской деятельности министерства культуры Нижегородской области.</p>
        <p>Теперь немного о достигнутых результатах. В сосновых стенах комплекса сегодня продолжается все наработанное предками нынешних городчан и создаются новые уникальные изделия. Здесь высоко чтят и бережно хранят ремесленные традиции, передавая их из поколения в поколение.</p>

    </section>

