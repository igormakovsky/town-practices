        <h2>«Город мастеров» и малый туристический бизнес</h2>
        <p>Все основные направления работы МТК «Город мастеров», и в первую очередь поддержка и развитие традиционных народных промыслов и ремесел, по умолчанию предполагают поддержку малого туристического бизнеса. Численность сотрудников самого комплекса&nbsp;— 135 человек (с учетом внешних совместителей), но динамично растущий турпоток обеспечивает занятость и получение дохода гораздо большего числа людей.</p>
        <p>Число посетителей «Города мастеров» выросло за несколько лет с 34,5 тысяч (2010 год) до 83 тысяч по итогам 2016 года. </p>
        <p>Набор услуг, предлагаемых туристу, впечатляет. Помимо различных экскурсий и множества мастер-классов это еще и целый спектр анимационых и фольклорных программ.</p>

