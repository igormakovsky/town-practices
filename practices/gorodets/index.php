    <section class="content text">

        <h2>«За» и «против» развития туризма</h2>
        <p>С Волги Городец смотрится очень впечатляюще. Высокие горы усеяны небольшими деревянными и кирпичными домиками, а также белоснежными храмами. С речной глади гостям открываются и удивительные сказочные рубленые терема, объединенные в единый комплекс. В Городце не встретишь бетонных высоток и не бывает пробок.</p>
        <p>Население города — чуть более 30 тыс. человек. Традиционные отрасли местной промышленности: судостроение (судоверфь, судоремонтный завод), деревообработка, художественные промыслы (фабрики городецкой росписи и строчки-вышивки, предприятие по выпечке печатных пряников), а также переработка сельскохозяйственной продукции (молокозавод ЗАО «Молоко», РайПо, Пекарня и т.д.). В наше время в Городце частным предприятием был построен первый в истории современной России круизный теплоход «Сура».</p>
        <p>В пользу развития города как крупного туристического центра региона говорил и его постепенно сложившийся имидж города-музея. Это единственный из городов Нижегородской области, имеющий свой музейный квартал. Рядом с краеведческим музеем расположено еще несколько музеев: Детский музей, музеи «Городецкий пряник», «Дом графини Паниной», Музей самоваров, Музей добра и, конечно же, «Город мастеров».</p>
        <p>Создание в Городце музейно-туристического комплекса «Город мастеров» в 2009 году было обсуловлено существованием такого понятия, как «городецкий феномен». Главное сокровище города — его талантливые жители и их потрясающе разнообразные таланты. Уникальность этой территории заключается в том, что на ней зародились, сохраняются и продолжают развиваться многочисленные народные промыслы: городецкая резьба по дереву, в том числе глухая, городецкая сюжетная роспись, городецкая золотная вышивка, гончарный промысел, изготовление жбанниковской свистульки, печатание медовых пряников, ткачество и плетение из корней сосны.</p>
        <p>Никаких аргументов против развития туризма как основного экономического и культурного стержня изначально не просматривалось, ни на местном, ни на региональном уровнях.</p>

